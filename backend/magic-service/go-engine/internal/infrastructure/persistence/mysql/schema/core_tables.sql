CREATE TABLE IF NOT EXISTS magic_flow_knowledge (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    code VARCHAR(255) NOT NULL,
    version INT NOT NULL DEFAULT 1,
    name VARCHAR(255) NOT NULL DEFAULT '',
    description VARCHAR(1000) NOT NULL DEFAULT '',
    type INT NOT NULL DEFAULT 1,
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    business_id VARCHAR(255) NOT NULL DEFAULT '',
    sync_status INT NOT NULL DEFAULT 0,
    sync_status_message VARCHAR(1000) NOT NULL DEFAULT '',
    model VARCHAR(255) NOT NULL DEFAULT '',
    vector_db VARCHAR(255) NOT NULL DEFAULT '',
    organization_code VARCHAR(255) NOT NULL DEFAULT '',
    created_uid VARCHAR(255) NOT NULL DEFAULT '',
    updated_uid VARCHAR(255) NOT NULL DEFAULT '',
    expected_num INT NOT NULL DEFAULT 0,
    completed_num INT NOT NULL DEFAULT 0,
    retrieve_config JSON NULL,
    fragment_config JSON NULL,
    embedding_config JSON NULL,
    word_count BIGINT NOT NULL DEFAULT 0,
    icon VARCHAR(255) NOT NULL DEFAULT '',
    source_type INT NULL,
    knowledge_base_type VARCHAR(64) NOT NULL DEFAULT 'flow_vector',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at DATETIME NULL,
    UNIQUE KEY uk_magic_flow_knowledge_code (code),
    KEY idx_combined (organization_code, type, updated_at),
    KEY idx_magic_flow_knowledge_org_business_id (organization_code, business_id, id)
);

CREATE TABLE IF NOT EXISTS knowledge_base_documents (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    organization_code VARCHAR(255) NOT NULL,
    knowledge_base_code VARCHAR(255) NOT NULL,
    source_binding_id BIGINT NOT NULL DEFAULT 0,
    source_item_id BIGINT NOT NULL DEFAULT 0,
    auto_added BOOLEAN NOT NULL DEFAULT FALSE,
    name VARCHAR(255) NOT NULL,
    description VARCHAR(255) NOT NULL DEFAULT '',
    code VARCHAR(255) NOT NULL,
    version INT UNSIGNED NOT NULL DEFAULT 1,
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    doc_type INT UNSIGNED NOT NULL,
    doc_metadata JSON NULL,
    document_file JSON NULL,
    sync_status INT NOT NULL DEFAULT 0,
    sync_times INT NOT NULL DEFAULT 0,
    sync_status_message VARCHAR(1000) NOT NULL DEFAULT '',
    embedding_model VARCHAR(255) NOT NULL DEFAULT '',
    vector_db VARCHAR(255) NOT NULL DEFAULT '',
    retrieve_config JSON NULL,
    fragment_config JSON NULL,
    embedding_config JSON NULL,
    vector_db_config JSON NULL,
    word_count BIGINT UNSIGNED NOT NULL DEFAULT 0,
    created_uid VARCHAR(255) NOT NULL DEFAULT '',
    updated_uid VARCHAR(255) NOT NULL DEFAULT '',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at DATETIME NULL,
    third_platform_type VARCHAR(255) NULL,
    third_file_id VARCHAR(255) NULL,
    UNIQUE KEY unique_knowledge_base_code_code_version (knowledge_base_code, code, version),
    KEY idx_kb_documents_code_id (code, id),
    KEY idx_kb_documents_org_kb (organization_code, knowledge_base_code),
    KEY idx_kb_documents_kb_third_file (third_platform_type(64), third_file_id(191), knowledge_base_code(191)),
    KEY idx_kb_documents_org_third_file (third_platform_type(64), third_file_id(191), organization_code(191)),
    KEY idx_kb_documents_source_binding_id_id (source_binding_id, id),
    KEY idx_kb_documents_source_item_id_id (source_item_id, id)
);

CREATE TABLE IF NOT EXISTS knowledge_source_bindings (
