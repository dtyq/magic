CREATE TABLE IF NOT EXISTS magic_flow_knowledge_fragment (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    knowledge_code VARCHAR(255) NOT NULL DEFAULT '',
    document_code VARCHAR(255) NOT NULL DEFAULT '',
    parent_fragment_id BIGINT UNSIGNED NULL,
    version INT UNSIGNED NOT NULL DEFAULT 1,
    content TEXT NOT NULL,
    metadata JSON NULL,
    business_id VARCHAR(255) NOT NULL DEFAULT '',
    sync_status INT NOT NULL DEFAULT 0,
    sync_times INT NOT NULL DEFAULT 0,
    sync_status_message VARCHAR(1000) NOT NULL DEFAULT '',
    point_id VARCHAR(255) NOT NULL DEFAULT '',
    vector TEXT NULL,
    word_count BIGINT UNSIGNED NOT NULL DEFAULT 0,
    created_uid VARCHAR(255) NOT NULL DEFAULT '',
    updated_uid VARCHAR(255) NOT NULL DEFAULT '',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at DATETIME NULL,
    KEY magic_flow_knowledge_fragment_knowledge_code_updated_at_index (knowledge_code, updated_at),
    KEY idx_knowledge_document_deleted_id (knowledge_code, document_code, deleted_at, id),
    KEY idx_knowledge_code_business_id (knowledge_code, business_id),
    KEY idx_fragment_point_id_id (point_id, id)
);

